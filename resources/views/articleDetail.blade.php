         <p>Ratusan kios Ayam Gepuk Pak Gembus tersebut tersebar di berbagai kota besar di Indonesia. Seluruh kawasan Jakarta dan sekitarnya seperti Tangerang, Depok, Bogor serta Bekasi sudah dirambah oleh produk milik lulusan D3 Administrasi Bisni Politeknik Negeri Semarang  ini. Ayam Gepuk Pak Gembus juga telah berhasil menembus pasar luar Jawa dan luar negeri. Kiosnya kini sudah mencapai Lampung, Manado, Jambi, Palembang, Malaysia, Singapura, Hong Kong dan Filipina. Lalu, apa yang membuat Ayam Gepuk Pak Gembus ini menjadi begitu istimewa? Padahal, sajian ayam bukan hal yang baru dalam ranah pasar kuliner Indonesia. Lagi, Ayam Gepuk Pak Gembus tidak tampil dengan konsep rumit atau kekinian yang saat ini sedang marak. Jawabannya, rasanya bisa Foodee temukan dalam seporsi nasi ayam dan sambal dari Ayam Gepuk Pak Gembus. Seporsi nasi hangat dengan ayam goreng yang sudah diungkep terlebih dahulu sehingga meresap bumbunya, digeprek dan disajikan dengan sambal istimewa. Foodee bisa memilih level pedas untuk sambalnya mulai dari yang tidak terlalu pedas dengan 1-2 cabai sampai yang pedasnya bikin Foodee terengah-engah dengan 9-10 cabai. Bukan cuma soal level pedas yang bisa dipilih, komposisi sambal Ayam Gepuk Pak Gembus lah yang berhasil memikat para pecinta ayam penyet. Bawang putih, cabe rawit, minyak wijen, garam, dan tambahan kacang mede diulek mentah-mentah dan setelah halus, disiram dengan minyak panas. Rasa, pedas, gurih dan manis, dari kacang mede, tercampur sempurna menghasilkan citarasa sambal yang membuat ketagihan. Selain soal rasa, harga yang bersahabat pun jadi nilai tambah ayam gepuk ini. Seporsi ayam gepuk dengan nasi, dihargai Rp 17.000 saja. Sangat terjangkau bahkan di penghujung bulan. Selain ayam, Foodee bisa memilih lauk lainnya seperti usus goreng, jeroan, tempe dan tahu. Lokasinya yang tersebar di banyak tempat pun menjadikan ayam gepuk pedas ini mudah dijangkau.Ditambah lagi dengan adanya aplikasi pesan makan antar, Ayam Gepuk Pak Gembus jadi makin mudah didapatkan. </p><p> Source: Foody </p>
