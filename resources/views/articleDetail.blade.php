                               <h4> Ayam Gepuk Pak Gembus </h4>
                              Ayam Gepuk Pak Gembus beberapa bulan terakhir menjadi perbincangan. Kesuksesan produk olahan ayam milik Ridho Nurul Adityawan, yang punya nama kecil ‘Gembus’, ini bisa dibilang fenomenal hingga diganjar penghargaan “Rising Business Award 2017” oleh Trans N Co Research. Pasalnya, olahan sederhana berupa ayam goreng dengan sambal bawang ini kini punya sekitar dari 462 cabang di seluruh kawasan Asia Tenggara. Pertama kali dibuka pada tahun 2013, awalnya Ayam Gepuk Pak Gembus hanya memiliki sebuah kios di Jalan Pasanggrahan, Jakarta dengan bermodal tenda berukuran 3x3 meter. Tiap harinya, Ridho hanya mampu menghabiskan tiga ekor ayam. Keputusan menjadikan usahanya menjadi franchise yang akhirnya mengantarkan Ayam Gepuk Pak Gembus memiliki cabang yang menjamur. <br>
